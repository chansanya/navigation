#!/usr/bin/env node

import { execFileSync } from 'node:child_process'
import { existsSync, mkdirSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { tmpdir } from 'node:os'

const DEFAULT_DB_NAME = process.env.D1_DATABASE_NAME || 'navigation_db'
const DEFAULT_OUTPUT = 'scripts/sql/remote-data.sql'

// 只同步业务表。删除时先删子表，插入时先插父表。
const DELETE_ORDER = ['shortcuts', 'site_submissions', 'sites', 'settings', 'categories']
const INSERT_ORDER = ['categories', 'settings', 'sites', 'site_submissions', 'shortcuts']
const DEFAULT_TABLES = INSERT_ORDER

function printHelp() {
  console.log(`
用法:
  node scripts/export-remote-d1.js [options]

参数:
  --db <name>           远程 D1 数据库名称，默认 ${DEFAULT_DB_NAME}，也可通过 D1_DATABASE_NAME 设置
  --out <path>          SQL 输出文件路径。默认输出到 ${DEFAULT_OUTPUT}
  --tables <a,b,c>      只导出指定表，多个表名用英文逗号分隔
  --help                显示帮助信息

用途:
  从远程 D1 拉取数据，生成可同步到本地 D1 的 SQL。
  生成的 SQL 会先 DELETE 再 INSERT，并按外键关系排序。
  同步到本地 D1 可直接使用 npm run sync:local-db。
`)
}

function parseArgs(argv) {
  const options = {
    db: DEFAULT_DB_NAME,
    out: DEFAULT_OUTPUT,
    tables: []
  }

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index]

    if (arg === '--help' || arg === '-h') {
      printHelp()
      process.exit(0)
    }

    if (arg === '--db') {
      options.db = argv[++index] || ''
      continue
    }

    if (arg === '--out') {
      options.out = argv[++index] || ''
      continue
    }

    if (arg === '--tables') {
      options.tables = (argv[++index] || '')
        .split(',')
        .map((table) => table.trim())
        .filter(Boolean)
      continue
    }

    throw new Error(`未知参数: ${arg}`)
  }

  return options
}

function orderIndex(table, order, fallbackIndex) {
  const index = order.indexOf(table)
  return index >= 0 ? index : order.length + fallbackIndex
}

function sortTables(tables, order) {
  return [...tables].sort((a, b) => (
    orderIndex(a, order, tables.indexOf(a)) - orderIndex(b, order, tables.indexOf(b))
  ))
}

function getTables(selectedTables) {
  if (selectedTables.length === 0) return DEFAULT_TABLES

  const knownTables = new Set(DEFAULT_TABLES)
  const unknownTables = selectedTables.filter((table) => !knownTables.has(table))
  if (unknownTables.length > 0) {
    throw new Error(`不支持的表: ${unknownTables.join(', ')}。当前脚本只导出业务表: ${DEFAULT_TABLES.join(', ')}`)
  }

  return DEFAULT_TABLES.filter((table) => selectedTables.includes(table))
}

function quoteIdentifier(identifier) {
  return `"${identifier.replaceAll('"', '""')}"`
}

function runWranglerExport(dbName, table, outputFile) {
  execFileSync('npx', [
    'wrangler',
    'd1',
    'export',
    dbName,
    '--remote',
    '--no-schema',
    '--table',
    table,
    '--output',
    outputFile
  ], {
    stdio: 'inherit'
  })
}

function stripUnsupportedSql(sql) {
  return sql
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line.length > 0)
    .filter((line) => !/^(BEGIN(\s+TRANSACTION)?|COMMIT|SAVEPOINT|ROLLBACK|PRAGMA)([\s;=]|$)/i.test(line))
    .join('\n')
}

function exportTable(dbName, table, tempDir) {
  const tempFile = join(tempDir, `${table}.sql`)
  runWranglerExport(dbName, table, tempFile)

  if (!existsSync(tempFile)) return ''
  return stripUnsupportedSql(readFileSync(tempFile, 'utf8'))
}

function buildExportSql(dbName, tables, exportedSqlByTable) {
  const sections = [
    '-- Generated by scripts/export-remote-d1.js',
    `-- Source: remote D1 ${dbName}`,
    `-- Generated at: ${new Date().toISOString()}`,
    '-- Compatible with: wrangler d1 execute --local --file',
    '',
    '-- Delete existing data',
    ''
  ]

  for (const table of sortTables(tables, DELETE_ORDER)) {
    sections.push(`-- Table: ${table}`)
    sections.push(`DELETE FROM ${quoteIdentifier(table)};`)
    sections.push('')
  }

  sections.push('-- Insert exported data')
  sections.push('')

  for (const table of sortTables(tables, INSERT_ORDER)) {
    const sql = exportedSqlByTable.get(table)
    if (!sql) continue
    sections.push(`-- Table: ${table}`)
    sections.push(sql)
    sections.push('')
  }

  return sections.join('\n')
}

function main() {
  const options = parseArgs(process.argv.slice(2))
  if (!options.db) {
    throw new Error('D1 数据库名称不能为空')
  }

  const tables = getTables(options.tables)
  const tempDir = mkdtempSync(join(tmpdir(), 'navigation-remote-d1-'))
  const exportedSqlByTable = new Map()

  try {
    for (const table of sortTables(tables, INSERT_ORDER)) {
      console.log(`导出远程表: ${table}`)
      exportedSqlByTable.set(table, exportTable(options.db, table, tempDir))
    }
  } finally {
    rmSync(tempDir, { recursive: true, force: true })
  }

  const outputPath = resolve(options.out || DEFAULT_OUTPUT)
  mkdirSync(dirname(outputPath), { recursive: true })
  writeFileSync(outputPath, buildExportSql(options.db, tables, exportedSqlByTable))

  console.log(`已从远程 D1 ${options.db} 导出 ${tables.length} 张表`)
  console.log(`输出文件: ${outputPath}`)
}

try {
  main()
} catch (error) {
  console.error(error instanceof Error ? error.message : error)
  process.exit(1)
}
